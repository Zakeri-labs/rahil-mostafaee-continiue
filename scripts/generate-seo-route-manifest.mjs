import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { getIndexableRoutes } from "../lib/seo/get-indexable-routes.mjs";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const routes = await getIndexableRoutes(path.join(projectRoot, "app"));
const outputPath = path.join(projectRoot, "lib/seo/generated-routes.ts");
const generatedRoutes = JSON.stringify(routes, null, 2).replace(/\n\]$/, ",\n]");

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(
  outputPath,
  [
    "// This file is generated by scripts/generate-seo-route-manifest.mjs.",
    "// Do not edit this route list manually; it is refreshed before every production build.",
    `export const INDEXABLE_ROUTE_PATHS = ${generatedRoutes} as const;`,
    "",
  ].join("\n"),
  "utf8",
);

console.log(`Generated ${routes.length} indexable route(s) in lib/seo/generated-routes.ts`);
